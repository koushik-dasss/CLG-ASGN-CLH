package ClassworkCodes;

public class DataTypeExamples {
    public static void main(String[] args){
        byte v1 = 35;
        System.out.println("The byte value is :"+ v1);
        int v2 = 2000;
        System.out.println("The int value is :"+ v2);
        short v3 = 20000;
        System.out.println("The short value is :"+ v3);
        long v4 = 345678; /*  L/l suffix is not to be used compulsorily while declaring a variable of a long type
         according to  modern compiler */  
        System.out.println("The long value is :"+ v4);
        long v5 = 345678l;
        System.out.println("The long value is :"+ v5);
        long v6 = 345678L;
        System.out.println("The long value is :"+ v6);
        float v7 = 2.111f; // F/f  suffix is compulsory
        System.out.println("The float value is :"+ v7);
        float v8 = 2.111F; // F/f suffix is  compulsory
        System.out.println("The float value is :"+ v8);
        double v9 = 2.11111111d; // D/d suffix is not  compulsory
        System.out.println("The double  value is :"+ v9);
        double v10 = 2.1111111111111D; // D/d suffix is not   compulsory
        System.out.println("The double  value is :"+ v10);
        double v11 = 2.111111111111111111111111111111; // D/d suffix is  not  compulsory
        System.out.println("The double  value is :"+ v11);
        boolean v12 = true;
        System.out.println("The boolean  value is :"+ v12);
        boolean v13 = false;
        System.out.println("The boolean  value is :"+ v13);
        char ch ='A';
        System.out.println("The character  value is :"+ ch);
        





        



        




        









    }


    
}
