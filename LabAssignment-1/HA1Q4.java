public class HA1Q4 {
    public static void main(String[] args){
        double a = 3.14159;
        System.out.println(a); // 3.14159
        System.out.println(a+1); // 3.14159 + 1 = 4.14159
        System.out.println(8/(int) a); // 8/(int) (3.14159) = 8/3 = 2 
        System.out.println(8/a); // 8/3.14159 =  2.5464812403910124
        System.out.println((int) (8/a)); // (int)(8/3.14159) = 2

    }
    
}
