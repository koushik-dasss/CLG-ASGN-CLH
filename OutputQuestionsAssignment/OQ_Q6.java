package OutputQuestionsAssignment;

public class OQ_Q6 {
    public static void main(String[] args) {
        // Right shift operator (>>)

        // Formula:- a >> b , result = a/2^b
        int x = - 4;
        System.out.println(x>>1);  // -4/2^1 = -4/2 = -2
        int y = 4;
        System.out.println(y>>1);  //  4/2^1 = 4/2 = 2
       
    }
    
}
