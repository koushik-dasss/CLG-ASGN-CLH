public class OQ_Q9 {
    public static void main(String[] args) {
        int i = 4;
        int j = 21;
        int k = ++i * 7 + 2 - j--; // 5 * 7 + 2 - 21 = 35 + 2 - 21 = 16 , i = 5 , j = 20 
        System.out.println("k = " + k); // k = 16 
        
    }
    
}
