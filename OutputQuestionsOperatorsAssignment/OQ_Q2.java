package OutputQuestionsOperatorsAssignment;

public class OQ_Q2 {
    public static void main(String[] args) {
        int a, b, c;
            a=-3+2*7-4; // -3 + 14 - 4 = 14 - 7 = 7 
            b=a*8+4%5-6; // 7 * 8 + 4 % 5 - 6 = 56 + 4 - 6 = 60 - 6 = 54 
            c=a+b*3-2%5-4; // 7 + 54 * 3 - 2 % 5 - 4 = 7 + 162 - 2 % 5 - 4 = 7 + 162 - 2 - 4 = 169 - 6 = 163
            System.out.println(a+" "+b+" "+c); // 7 54 163
    }
    
}
