package OutputQuestions;

public class IncrementDecrementOperator {
    public static void main(String[] args) {
        int x = 5 , y;
        y = ++x;
        System.out.println(x + "\t" + y);
        y = x++;
        System.out.println(x + "\t" + y);
        y = --x;
        System.out.println(x + "\t" + y);
        y = x--;
        System.out.println(x + "\t" + y);



        
    }
    
}
